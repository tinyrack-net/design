import { mkdir, rm, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const marker = 'Generated by scripts/generate-showcase-stories.mjs';

const registries = [
  {
    library: 'mantine',
    titlePrefix: 'Mantine',
    source: resolve(root, 'src/showcase/mantine-showcase.tsx'),
    exportName: 'mantineShowcaseEntries',
    idPrefix: 'mantine-',
  },
  {
    library: 'daisyui',
    titlePrefix: 'daisyUI',
    source: resolve(root, 'src/showcase/daisyui-showcase.tsx'),
    exportName: 'daisyUiShowcaseEntries',
    idPrefix: 'daisyui-',
  },
];

function componentName(name) {
  const cleaned = name.replace(/[^A-Za-z0-9_$]/g, '');
  const normalized = cleaned[0]?.toUpperCase() + cleaned.slice(1);
  return `${normalized || 'Showcase'}Story`;
}

async function readText(path) {
  return await import('node:fs/promises').then(({ readFile }) =>
    readFile(path, 'utf8'),
  );
}

function extractEntries(sourceText) {
  return [...sourceText.matchAll(/id: '([^']+)',\s+name: '([^']+)'/g)].map(
    ([, id, name]) => ({ id, name }),
  );
}

function storySource({ entry, registry }) {
  const storyComponent = componentName(entry.name);
  return `// ${marker}\nimport '../../../src/showcase/showcase.css';\nimport type { Meta, StoryObj } from '@storybook/react-vite';\nimport {\n  ${registry.exportName},\n  SingleShowcaseStory,\n} from '../../../src/showcase/index.js';\nimport type { ShowcaseScenarioId } from '../../../src/showcase/index.js';\n\ntype ComponentStoryProps = {\n  scenarioId?: ShowcaseScenarioId;\n};\n\nconst entry = ${registry.exportName}.find(\n  (showcaseEntry) => showcaseEntry.id === '${entry.id}',\n);\n\nif (!entry) {\n  throw new Error('Missing showcase entry: ${entry.id}');\n}\n\nfunction ${storyComponent}({ scenarioId = 'preview' }: ComponentStoryProps) {\n  return (\n    <SingleShowcaseStory\n      entry={entry}\n      library="${registry.library}"\n      scenarioId={scenarioId}\n    />\n  );\n}\n\nconst meta = {\n  title: '${registry.titlePrefix}/Components/${entry.name}',\n  component: ${storyComponent},\n  args: {\n    scenarioId: 'preview',\n  },\n  argTypes: {\n    scenarioId: {\n      control: 'select',\n      options: ['preview', 'variants'],\n      description: 'Switch between the default component preview and the variant matrix.',\n    },\n  },\n  parameters: {\n    layout: 'centered',\n  },\n} satisfies Meta<typeof ${storyComponent}>;\n\nexport default meta;\n\ntype Story = StoryObj<typeof meta>;\n\nexport const Preview: Story = {\n  args: {\n    scenarioId: 'preview',\n  },\n};\n\nexport const Variants: Story = {\n  args: {\n    scenarioId: 'variants',\n  },\n};\n`;
}

for (const registry of registries) {
  const sourceText = await readText(registry.source);
  const entries = extractEntries(sourceText);
  const outDir = resolve(root, 'stories', registry.library, 'components');

  await rm(outDir, { recursive: true, force: true });
  await mkdir(outDir, { recursive: true });

  await Promise.all(
    entries.map((entry) => {
      const slug = entry.id.replace(registry.idPrefix, '');
      return writeFile(
        resolve(outDir, `${slug}.stories.tsx`),
        storySource({ entry, registry }),
      );
    }),
  );

  console.log(`generated ${entries.length} ${registry.library} component stories`);
}
