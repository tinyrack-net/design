import { mkdir, rm, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const marker = 'Generated by scripts/generate-showcase-stories.mjs';

const scenarios = [
  {
    exportName: 'Preview',
    id: 'preview',
    description: 'Recommended default usage for the component.',
  },
  {
    exportName: 'Variants',
    id: 'variants',
    description: 'Supported visual variants and key style axes.',
  },
  {
    exportName: 'States',
    id: 'states',
    description: 'Interactive and validation states.',
  },
  {
    exportName: 'Composition',
    id: 'composition',
    description: 'Real product UI compositions and adjacent components.',
  },
  {
    exportName: 'Tokens',
    id: 'tokens',
    description: 'Design tokens used by this component.',
  },
  {
    exportName: 'Accessibility',
    id: 'accessibility',
    description: 'Keyboard, ARIA, labeling, and contrast notes.',
  },
  {
    exportName: 'Playground',
    id: 'playground',
    description: 'Interactive controls for quick exploration.',
  },
];

const registries = [
  {
    library: 'mantine',
    titlePrefix: 'Mantine',
    source: resolve(root, 'src/showcase/mantine-showcase.tsx'),
    exportName: 'mantineShowcaseEntries',
    idPrefix: 'mantine-',
  },
  {
    library: 'daisyui',
    titlePrefix: 'daisyUI',
    source: resolve(root, 'src/showcase/daisyui-showcase.tsx'),
    exportName: 'daisyUiShowcaseEntries',
    idPrefix: 'daisyui-',
  },
];

function componentName(name) {
  const cleaned = name.replace(/[^A-Za-z0-9_$]/g, '');
  const normalized = cleaned[0]?.toUpperCase() + cleaned.slice(1);
  return `${normalized || 'Showcase'}Story`;
}

async function readText(path) {
  return await import('node:fs/promises').then(({ readFile }) =>
    readFile(path, 'utf8'),
  );
}

function extractEntries(sourceText) {
  return [...sourceText.matchAll(/id: '([^']+)',\s+name: '([^']+)'/g)].map(
    ([, id, name]) => ({ id, name }),
  );
}

function storySource({ entry, registry }) {
  const storyComponent = componentName(entry.name);
  const options = scenarios.map((scenario) => `        '${scenario.id}',`).join('\n');
  const storyExports = scenarios
    .map(
      (scenario) => `export const ${scenario.exportName}: Story = {
  args: {
    scenarioId: '${scenario.id}',
  },
  parameters: {
    docs: {
      description: {
        story: '${scenario.description}',
      },
    },
  },
};`,
    )
    .join('\n\n');

  return `// ${marker}\nimport '../../../src/showcase/showcase.css';\nimport type { Meta, StoryObj } from '@storybook/react-vite';\nimport type { ShowcaseScenarioId } from '../../../src/showcase/index.js';\nimport {\n  ${registry.exportName},\n  SingleShowcaseStory,\n} from '../../../src/showcase/index.js';\n\ntype ComponentStoryProps = {\n  scenarioId?: ShowcaseScenarioId;\n};\n\nconst entry = ${registry.exportName}.find(\n  (showcaseEntry) => showcaseEntry.id === '${entry.id}',\n);\n\nif (!entry) {\n  throw new Error('Missing showcase entry: ${entry.id}');\n}\n\nfunction ${storyComponent}({ scenarioId = 'preview' }: ComponentStoryProps) {\n  return (\n    <SingleShowcaseStory entry={entry} library="${registry.library}" scenarioId={scenarioId} />\n  );\n}\n\nconst meta = {\n  title: '${registry.titlePrefix}/Components/${entry.name}',\n  component: ${storyComponent},\n  args: {\n    scenarioId: 'preview',\n  },\n  argTypes: {\n    scenarioId: {\n      control: 'select',\n      options: [\n${options}\n      ],\n      description: 'Switch between component documentation scenarios.',\n    },\n  },\n  parameters: {\n    layout: 'fullscreen',\n  },\n} satisfies Meta<typeof ${storyComponent}>;\n\nexport default meta;\n\ntype Story = StoryObj<typeof meta>;\n\n${storyExports}\n`;
}

for (const registry of registries) {
  const sourceText = await readText(registry.source);
  const entries = extractEntries(sourceText);
  const outDir = resolve(root, 'stories', registry.library, 'components');

  await rm(outDir, { recursive: true, force: true });
  await mkdir(outDir, { recursive: true });

  await Promise.all(
    entries.map((entry) => {
      const slug = entry.id.replace(registry.idPrefix, '');
      return writeFile(
        resolve(outDir, `${slug}.stories.tsx`),
        storySource({ entry, registry }),
      );
    }),
  );

  console.log(`generated ${entries.length} ${registry.library} component stories`);
}
